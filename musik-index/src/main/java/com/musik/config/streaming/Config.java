package com.musik.config.streaming;

import com.musik.config.Argument;

public class Config {
    @Argument
    private String host;

    @Argument
    private String port;

    @Argument
    private String db;

    @Argument
    private String table;

    @Argument
    private String username;

    @Argument
    private String password;

    @Argument(value = "kafka")
    private String kafkaServers;

    @Argument
    private String zookeeperServers;

    @Argument(value = "topics")
    private String kafkaTopics;

    @Argument(value = "group_id")
    private String groupId;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getDb() {
        return db;
    }

    public void setDb(String db) {
        this.db = db;
    }

    public String getTable() {
        return table;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getKafkaServers() {
        return kafkaServers;
    }

    public void setKafkaServers(String kafkaServers) {
        this.kafkaServers = kafkaServers;
    }

    public String getZookeeperServers() {
        return zookeeperServers;
    }

    public void setZookeeperServers(String zookeeperServers) {
        this.zookeeperServers = zookeeperServers;
    }

    public String getKafkaTopics() {
        return kafkaTopics;
    }

    public void setKafkaTopics(String kafkaTopics) {
        this.kafkaTopics = kafkaTopics;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }
}